#include <stdio.h>

int main()
{
    // Data types like int(integers),float(real numbers),char(characters / a to z)
    int age = 22;
    printf("My age is %d \n", age);
    return 0;

    float pi = 3.14;
    printf("The value of pi is %f \n", pi);
    return 0;

    // Always we write character in singal qoute.
    char star = '*';
    printf("the star is %c \n", star);
    return 0;
}